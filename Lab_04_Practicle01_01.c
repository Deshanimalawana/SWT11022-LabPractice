#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x=10, y=4;
    int sum = x+y;
    int defference = x-y;
    int product = x*y;
    int quotient = x/y;
    int remainder = x%y;

    printf("Sum: %d\n", sum);
    printf("defference: %d\n", defference);
    printf("product: %d\n", product);
    printf("quotient: %d\n", quotient);
    printf("remainder: %d\n", remainder);

    return 0;
}
