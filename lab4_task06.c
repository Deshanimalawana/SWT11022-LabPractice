#include<stdio.h>

int main() {
    int x = 12;
    int y = 6;

    int andResult = x & y;
    int orResult= x | y;
    int xorResult = x ^ y;
    int notResult = ~x;
    int leftShift = x << 2;
    int rightShift = y >> 1;



    printf("AND Results: %d\n", andResult);
    printf("OR Results: %d\n", orResult);
    printf("XOR Results: %d\n", xorResult);
    printf("NOT Results: %d\n", notResult);
    printf("Left Shift Results: %d\n", leftshift);
    printf("Right Shift Results: %d\n", rightshift);

    return 0;











}
