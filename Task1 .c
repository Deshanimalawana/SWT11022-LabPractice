#include <stdio.h>

int main() {
    // Variable declaration and initialization
    int id = 101;         // Integer data type
    char section = 'B';   // Character data type
    float marks = 89.5;   // Floating-point data type

    // Printing the output using printf()
    printf("ID: %d, Section: %c, Marks: %.2f\n", id, section, marks);

    return 0;
}
