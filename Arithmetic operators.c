#include <stdio.h>
int main () {

   int X=10 , Y=4;
   int sum = X+Y;
   int difference = X-Y;
   int product = X*Y;
   int quotient = X/Y;
   int remainder = X%Y;

   printf ("Sum: %d\n",sum);
   printf ("difference : %d\n",difference);
   printf ("product: %d\n",product);
   printf ("quotient: %d\n",quotient);
   printf ("remainder: %d\n",remainder);

   return 0;
}
