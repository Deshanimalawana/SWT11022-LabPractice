#include<stdio.h>
int main(){
    int x = 10, y = 20;

    printf("value of x:%d\n",x);
    printf("Value of x:%d\n",++x);
    printf("Value of x Incremented:%d\n",x);

    printf("Value of y:%d\n",y);
    printf("Value of y:%d\n",y++);
    printf("Value of Incremented y:%d\n",y);

    return 0;


}
