#include <stdio.h>
int main () {
   int X = 14;
   int Y = 5;

   int ANDoperation = X & Y;
   int ORoperation = X | Y;
   int XORoperation = X ^ Y;


   printf(" X AND Y is: %d\n",ANDoperation);
   printf ("X OR Y is : %d\n",ORoperation);
   printf ("X XOR Y is : %d\n\n\n",XORoperation);



   return 0;

}
